# Variáveis permite o armazenamento de informações na memória

minha_variavel = "Hello Word!"
print(minha_variavel) 
print(minha_variavel)
print(minha_variavel)

"""
  Sintaxe:
            Nome_da_variavel = Valor
            
  ˖ Nome da variável não deve ter caracteres especiais e nem espaços
  
  - É case sentive:
  
  ˖ nome_da_variavel
  ˖ Nome_da_variavel → é diferente
  
  ˖ [=] operador de atribuição
  
  ˖ Valor: define o tipo da variável
  
            Tipos de Dados:
            
    ˖ Numérico inteiro
        - 47
        
    ˖ Numérico flutuante
        - 12.89
    ˖ Textual(string)
        - " Olá Mundo!"
        
    ˖ Booleana
        - True(Verdadeiro) ou False(Falso)
  
"""

var1 = 1 # variável inteira
var2 = 1.1 # variável float
var3 = "Eu sou uma string" # variável string
var4 = True 
var4 = False

print(var1, var2, var3)
print(var2)
print(var3)
print(var4) # variáveis iguais quando é declarada pela segunda vez ela se sobreponhe o valor anterior